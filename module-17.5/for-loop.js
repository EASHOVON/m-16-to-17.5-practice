// For Loop, how forloop works, while vs for loop

// For Loop হইতেছে While loop এর চ্যাপ্টা ভার্সন

console.log('running a for loop');
for (var roastGiven = 0; roastGiven < 7; roastGiven++) {
    console.log('Roast den, Please!');
    console.log(roastGiven);
}

// For Loop কে শর্টকাট ভাবে লিখতে হয়....

for (var i = 0; i < 7; i++) {
    console.log(i);
}