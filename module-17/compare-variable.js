// Compare variables and comparision operator

// একটি ভ্যারিয়েবল এর সাথে আর একটি ভ্যারিয়েবল এর তুলনা
console.log(myLove > yourLove); // একটি আর একটির তুলনায় বড় কিনা

console.log(myLove < yourLove); // একটি আর একটির চেয়ে ছোট কিনা

console.log(myLove == yourLove); // একটি আর একটির সমান কিনা

console.log(myLove != yourLove); // একটি আর একটির অসমান কিনা

// একাধিক শর্ত যাচাই করার ক্ষেত্রে
console.log(studyGood && goodPerson); // অর্থাৎ এখানে দুটো শর্তই পূরণ করতে হবে।

// আর যেকোনো একটা কন্ডিশন পুরন করলেই চলবে এমন করতে হলে
console.log(isBanker || goodResult);