// Simple Mathematical Operations in JavaScript
var applePrice = 150;
var potatoPrice = 40;
var onionPrice = 50;
var totalPrice = applePrice + potatoPrice + onionPrice;
var appleWeight = 7;
var moneyOfPocket = 5000;
var totalAppleCost = applePrice * appleWeight;
var pocketMoneyVsTotalAppleCost = moneyOfPocket - totalAppleCost;
var totalPotatoWithMyPocketMoney = moneyOfPocket / potatoPrice;
console.log(totalPrice);
console.log(totalAppleCost);
console.log(pocketMoneyVsTotalAppleCost);
console.log(totalPotatoWithMyPocketMoney);
